    public function findTopSellingProducts(int $limit = 5) {
        $supplierCategories = $this->getSupplierCategories();

        $qb = $this->createQueryBuilder('p')
            ->select('p as product', 'SUM(oi.quantity) as totalQuantity')
            ->join('p.ProductClasses', 'pc')
            ->join('Eccube\Entity\OrderItem', 'oi', 'WITH', 'oi.ProductClass = pc.id')
            ->join('Eccube\Entity\Order', 'o', 'WITH', 'oi.Order = o.id')
            ->innerJoin('p.ProductCategories', 'pcat')
            ->where('o.OrderStatus IN (:statuses)')
            ->andWhere('p.Status = 1')
            ->setParameter('statuses', [5, 6])
            ->groupBy('p.id')
            ->orderBy('totalQuantity', 'DESC')
            ->setMaxResults($limit);

        if (!empty($supplierCategories)) {
            $qb->andWhere('pcat.Category NOT IN (:supplierCategories)')
            ->setParameter('supplierCategories', $supplierCategories);
        }

        $results = $qb->getQuery()->getResult();
        return array_map(function ($item) {
            return $item['product'];
        }, $results);
    }
