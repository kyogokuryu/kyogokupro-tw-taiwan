    public function parseFAQs($input) {
        $lines = preg_split("/\r\n|\n|\r/", $input);
        $faqs = [];
        $currentQ = '';
        $currentA = '';
        $mode = '';

        foreach ($lines as $line) {
            $line = trim($line);
            if ($this->startsWith($line, 'Q:')) {
                if ($currentQ && $currentA) {
                    $faqs[] = ['question' => trim($currentQ), 'answer' => trim($currentA)];
                    $currentA = '';
                }
                $currentQ = substr($line, 2) . "\n";
                $mode = 'Q';
            } elseif ($this->startsWith($line, 'A:')) {
                $currentA = substr($line, 2) . "\n";
                $mode = 'A';
            } else {
                if ($mode === 'Q') {
                    $currentQ .= $line . "\n";
                } elseif ($mode === 'A') {
                    $currentA .= $line . "\n";
                }
            }
        }

        if ($currentQ && $currentA) {
            $faqs[] = ['question' => trim($currentQ), 'answer' => trim($currentA)];
        }

        return $faqs;
    }

